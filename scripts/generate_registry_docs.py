import argparse
import sys
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from ai_workbench.core.manifest_loader import load_agent_manifest, load_capability_manifest


DEFAULT_OUTPUT = ROOT / "docs" / "generated" / "REGISTRY.md"


def generate_registry_markdown(
    agents_root: str | Path = ROOT / "agents",
    capabilities_root: str | Path = ROOT / "capabilities",
) -> str:
    agents = _load_agents(Path(agents_root))
    capabilities = _load_capabilities(Path(capabilities_root))
    lines = [
        "# Generated Registry",
        "",
        "Generated by:",
        "`uv run python scripts/generate_registry_docs.py`",
        "",
        "Do not edit manually.",
        "",
        "## Agents",
        "",
        "| id | name | type | actions | capabilities | entry / prompt | llm profile | config keys |",
        "| --- | --- | --- | --- | --- | --- | --- | --- |",
    ]
    for agent in sorted(agents, key=lambda item: item.id):
        lines.append(
            "| "
            + " | ".join(
                [
                    _cell(agent.id),
                    _cell(agent.name),
                    _cell(agent.type),
                    _cell(", ".join(action.id for action in agent.actions)),
                    _cell(", ".join(agent.capabilities)),
                    _cell(agent.entry if agent.type == "script" else _short_prompt(agent.prompt)),
                    _cell(_llm_profile(agent.llm)),
                    _cell(", ".join(field.name for field in agent.config_schema)),
                ]
            )
            + " |"
        )
    lines.extend(
        [
            "",
            "## Capabilities",
            "",
            "| id | name | methods | commands | output types | config keys |",
            "| --- | --- | --- | --- | --- | --- |",
        ]
    )
    for capability in sorted(capabilities, key=lambda item: item.id):
        lines.append(
            "| "
            + " | ".join(
                [
                    _cell(capability.id),
                    _cell(capability.name),
                    _cell(", ".join(method.id for method in capability.methods)),
                    _cell(", ".join(command.name for command in capability.commands)),
                    _cell(", ".join(_output_type(method.output) for method in capability.methods if _output_type(method.output))),
                    _cell(", ".join(field.name for field in capability.config_schema)),
                ]
            )
            + " |"
        )
    lines.extend(
        [
            "",
            "## Notes",
            "",
            "- This file is generated from manifests only.",
            "- It does not import runtime code.",
            "- Run `uv run python scripts/check_agents.py --strict` for runtime validation.",
            "",
        ]
    )
    return "\n".join(lines)


def write_registry(
    output: str | Path = DEFAULT_OUTPUT,
    agents_root: str | Path = ROOT / "agents",
    capabilities_root: str | Path = ROOT / "capabilities",
) -> str:
    content = generate_registry_markdown(agents_root=agents_root, capabilities_root=capabilities_root)
    output_path = Path(output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content, encoding="utf-8", newline="\n")
    return content


def check_registry(
    output: str | Path = DEFAULT_OUTPUT,
    agents_root: str | Path = ROOT / "agents",
    capabilities_root: str | Path = ROOT / "capabilities",
) -> bool:
    output_path = Path(output)
    expected = generate_registry_markdown(agents_root=agents_root, capabilities_root=capabilities_root)
    if not output_path.is_file():
        print(f"[FAIL] {output_path} does not exist. Run: uv run python scripts/generate_registry_docs.py")
        return False
    current = output_path.read_text(encoding="utf-8")
    if current != expected:
        print(f"[FAIL] {output_path} is out of date. Run: uv run python scripts/generate_registry_docs.py")
        return False
    print(f"[OK] {output_path} is up to date")
    return True


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Generate docs/generated/REGISTRY.md from manifests.")
    parser.add_argument("--check", action="store_true", help="fail if the generated registry is out of date")
    parser.add_argument("--agents-root", default=str(ROOT / "agents"), help=argparse.SUPPRESS)
    parser.add_argument("--capabilities-root", default=str(ROOT / "capabilities"), help=argparse.SUPPRESS)
    parser.add_argument("--output", default=str(DEFAULT_OUTPUT), help=argparse.SUPPRESS)
    args = parser.parse_args(argv)

    if args.check:
        return 0 if check_registry(args.output, args.agents_root, args.capabilities_root) else 1
    write_registry(args.output, args.agents_root, args.capabilities_root)
    print(f"[OK] Wrote {Path(args.output)}")
    return 0


def _load_agents(root: Path) -> list[Any]:
    agents = []
    for manifest_path in sorted(root.glob("*/agent.yaml")):
        try:
            agents.append(load_agent_manifest(manifest_path))
        except Exception as exc:
            raise SystemExit(f"[FAIL] {manifest_path}: invalid agent manifest: {exc}") from exc
    return agents


def _load_capabilities(root: Path) -> list[Any]:
    capabilities = []
    for manifest_path in sorted(root.glob("*/capability.yaml")):
        try:
            capabilities.append(load_capability_manifest(manifest_path))
        except Exception as exc:
            raise SystemExit(f"[FAIL] {manifest_path}: invalid capability manifest: {exc}") from exc
    return capabilities


def _short_prompt(prompt: str | None) -> str:
    if not prompt:
        return ""
    compact = " ".join(prompt.split())
    if len(compact) <= 60:
        return compact
    return f"{compact[:57]}..."


def _llm_profile(llm: dict[str, Any] | None) -> str:
    if not isinstance(llm, dict):
        return ""
    value = llm.get("profile")
    return str(value) if value is not None else ""


def _output_type(output: dict[str, Any]) -> str:
    if not isinstance(output, dict):
        return ""
    value = output.get("type")
    return str(value) if value is not None else ""


def _cell(value: Any) -> str:
    text = "" if value is None else str(value)
    return text.replace("|", "\\|").replace("\n", "<br>")


if __name__ == "__main__":
    raise SystemExit(main())
